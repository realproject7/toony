// Generate apps/studio/src/app/lettering-fonts.css from the @toony/fonts registry.
//
// The studio renders the SVG preview/editor with the SAME curated faces the
// export canvas registers (#56). Those `@font-face` rules must therefore stay in
// lock-step with the @toony/fonts registry — a hand-maintained CSS file silently
// drifts when a face is added/renamed/reweighted. This script is the single
// source: it emits `fontFacesCss("/fonts")` from the registry so the CSS is
// always exactly the registry's set, served from the studio's own /public/fonts
// (no CDN). It runs in predev/prebuild alongside copy-fonts.mjs.
//
// Re-run after changing the font registry: `node scripts/gen-studio-fonts-css.mjs`
// (or just `pnpm --filter @toony/studio dev` / `build`, which run it for you).

import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { fontFacesCss } from "@toony/fonts";

const studioRoot = join(dirname(fileURLToPath(import.meta.url)), "..");
const cssPath = join(studioRoot, "src", "app", "lettering-fonts.css");

const header = `/* GENERATED by scripts/gen-studio-fonts-css.mjs from the @toony/fonts registry.
 * Self-hosted curated OFL faces (served from /public/fonts, no CDN). Do not
 * edit by hand — re-run the generator after changing the font registry. */`;

// The woff2 files are served from /public/fonts by copy-fonts.mjs.
const body = fontFacesCss("/fonts");
const css = `${header}\n\n${body}\n`;

writeFileSync(cssPath, css, "utf8");
console.log(`gen-studio-fonts-css: wrote ${cssPath}`);
