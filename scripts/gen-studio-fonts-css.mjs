// Generate apps/studio/src/app/lettering-fonts.css from the @toony/fonts registry.
//
// The studio self-hosts the curated woff2 (copied into apps/studio/public/fonts)
// and injects these @font-face rules so the SVG preview/editor render with the
// SAME family names @toony/render resolves and @toony/export registers. Running
// this keeps the CSS in lockstep with the registry; the output is committed so
// `next build` needs no generation step. Build @toony/fonts first, then re-run
// after changing the registry:
//   pnpm --filter @toony/fonts build && node scripts/gen-studio-fonts-css.mjs
import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { fontFacesCss } from "../packages/fonts/dist/index.js";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const out = join(root, "apps/studio/src/app/lettering-fonts.css");
const header =
  "/* GENERATED by scripts/gen-studio-fonts-css.mjs from the @toony/fonts registry.\n" +
  " * Self-hosted curated OFL faces (served from /public/fonts, no CDN). Do not\n" +
  " * edit by hand — re-run the generator after changing the font registry. */\n\n";
writeFileSync(out, `${header}${fontFacesCss("/fonts")}\n`);
console.log("wrote", out);
